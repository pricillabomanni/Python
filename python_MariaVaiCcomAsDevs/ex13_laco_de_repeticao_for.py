
#incrementando multiplicação  no for
lista = [1, 2, 3, 4, 10]
for numero in lista:
  print(numero ** 2)


#Dividindo na iteração no for
lista = [2, 4, 6, 8, 10, 12]
for numero in lista:
  print(numero / 2)

#somando na iteração no for
lista = [2, 4, 6, 8, 10, 12]
for numero in lista:
  print(numero + 2)


#subtraindo na iteração no for
lista = [2, 4, 6, 8, 10, 12]
for numero in lista:
  print(numero - 2)


#String
lista = ["Pri", "Lini", "Mary", "Bruna"]
for nome in lista:
  print(nome + " Linda!")