# Até qual posição eu quero ir
for x in range(6):
  print(x)


# Fala que quero começar do 1 e ir até a posição 6
for x in range(1,6):
  print(x)


# Fala que quero começar do 4 e ir até a posição 9
for x in range(4,9):
  print(x)

# Fala que quero começar do 1 e ir até a posição 10, incrementando de 2 em 2.
for x in range(1, 10, 2):
  print(x)
  print(x)